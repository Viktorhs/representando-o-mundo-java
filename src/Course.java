public class Course {
  private String name;
  private String description;
  private int contentQuantity;
  private int exercisesQuantity;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public int getContentQuantity() {
    return contentQuantity;
  }

  public void setContentQuantity(int contentQuantity) {
    this.contentQuantity = contentQuantity;
  }

  public int getExercisesQuantity() {
    return exercisesQuantity;
  }

  public void setExercisesQuantity(int exercisesQuantity) {
    this.exercisesQuantity = exercisesQuantity;
  }

}